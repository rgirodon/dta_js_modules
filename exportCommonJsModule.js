function add(a, b) {

    return a + b
}

function minus(a, b) {

    return a - b
}

function multiply(a, b) {

    return a * b
}

function divide(a, b) {

    return a / b
}

module.exports.add = add;

module.exports.minus = minus;

module.exports.multiply = multiply;

module.exports.divide = divide;